    /**
     * Metodo que devuelve un TableModel con los datos de logs
     * @return 
     */
    public DefaultTableModel getHistorialTableModel(){
        // Obtener la lista de operaciones (la pila como lista)
        List<Operacion> operaciones = HistorialManager.getInstance().obtenerHistorial();
        
        
        
        // Definir columnas
        String[] columnas = {"Fecha/Hora", "Descripci√≥n"};
        DefaultTableModel model = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;  // La tabla NO se puede editar
            }
        };
        
        // Llenar filas con las operaciones
        for (Operacion op : operaciones) {
            Object[] fila = {
                op.getTimestamp().toString(),  // Formato: 2023-10-05T14:30:00
                op.getDescripcion()
            };
            model.addRow(fila);
        }
        
        return model;
    }

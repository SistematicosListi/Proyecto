    /**
     * Action para buscar el producto con la clave
     * @param evt 
     */
    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        String clave = txt_clave.getText().trim();

        if(txt_clave.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "El campo esta vacio");
            return;
        }

        

        Producto encontrado = inventario.buscarProducto(clave);

        if(encontrado != null){
            mostrarTabla(encontrado);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontro ningun vehiculo con esa matricula");
        }
        
    

    }//GEN-LAST:event_btn_buscarActionPerformed
    private void mostrarTabla(Producto p){
        String [] columnas = {"Clave", "Nombre", "Cantidad", "Precio"};
        DefaultTableModel model = new DefaultTableModel();
        model = new DefaultTableModel(new Object [0][0][0][0], columnas){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        if(p!= null){
            Object [] fila = {p.getClave(), p.getNombre(), p.getCantidad(), p.getPrecio()};
            model.addRow(fila);
        }
        
        jTable2.setModel(model);
    
    }
   

